	public static List<Underlying> getUnderlying(List<String> names) {
		
		List<Underlying> unders = new ArrayList<Underlying>();
		
		for(String name:names) {
			Underlying under = new Underlying(name);
			unders.add(under); 			
		}
		
		return unders;
	}
	
